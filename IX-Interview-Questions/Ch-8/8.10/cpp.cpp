/*
 * Assumes all rows are the same length
 *
 */
void paint_fill(vector<vector<int>>& screen, int r, int c, int color) {
    if ((r >= 0) && (c >= 0)
            && (r < screen.size()) && (c < screen[r].size())) {
        auto orig = screen[r][c];
        screen[r][c] = color;

        if (((r - 1) >= 0) && (screen[r - 1][c] == orig)) {
            paint_fill(screen, r - 1, c, color);
        }

        if (((c - 1) >= 0) && (screen[r][c - 1] == orig)) {
            paint_fill(screen, r, c - 1, color);
        }

        if (((r + 1) < screen.size()) && (screen[r + 1][c] == orig)) {
            paint_fill(screen, r + 1, c, color);
        }

        if (((c + 1) < screen[r].size()) && (screen[r][c + 1] == orig)) {
            paint_fill(screen, r, c + 1, color);
        }
    }
}

void print_screen(const vector<vector<int>>& screen) {
    for (auto& row : screen) {
        for (auto col : row) {
            cout << col;
        }
        cout << endl;
    }
    cout << endl;
}

